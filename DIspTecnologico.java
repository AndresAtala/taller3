public abstract class DIspTecnologico {

	private string marca;
	private string modelo;
	private string ram;
	private string almacenamiento;
	private string procesador;
	private string anoFabricacion;
	private string precio;
	private string strock;

	public string getMarca() {
		return this.marca;
	}

	/**
	 * 
	 * @param marca
	 */
	public void setMarca(string marca) {
		this.marca = marca;
	}

	public string getModelo() {
		return this.modelo;
	}

	/**
	 * 
	 * @param modelo
	 */
	public void setModelo(string modelo) {
		this.modelo = modelo;
	}

	public string getRam() {
		return this.ram;
	}

	/**
	 * 
	 * @param ram
	 */
	public void setRam(string ram) {
		this.ram = ram;
	}

	public string getAlmacenamiento() {
		return this.almacenamiento;
	}

	/**
	 * 
	 * @param almacenamiento
	 */
	public void setAlmacenamiento(string almacenamiento) {
		this.almacenamiento = almacenamiento;
	}

	public string getProcesador() {
		return this.procesador;
	}

	/**
	 * 
	 * @param procesador
	 */
	public void setProcesador(string procesador) {
		this.procesador = procesador;
	}

	public string getAnoFabricacion() {
		return this.anoFabricacion;
	}

	/**
	 * 
	 * @param anoFabricacion
	 */
	public void setAnoFabricacion(string anoFabricacion) {
		this.anoFabricacion = anoFabricacion;
	}

	public string getPrecio() {
		return this.precio;
	}

	/**
	 * 
	 * @param precio
	 */
	public void setPrecio(string precio) {
		this.precio = precio;
	}

	public string getStrock() {
		return this.strock;
	}

	/**
	 * 
	 * @param strock
	 */
	public void setStrock(string strock) {
		this.strock = strock;
	}

}